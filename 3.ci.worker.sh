#!/usr/bin/env bash

set -x
export CMD_PATH=$(cd `dirname $0`; pwd)
export PROJECT_NAME="${CMD_PATH##*/}"
echo $PROJECT_NAME
cd $CMD_PATH

echo "============================================================================"
pwd

echo "============================================================================"
whoami

echo "============================================================================"
env

echo "============================================================================"

for ci_dir in `ls -d ci/ci-*`
do
    echo $ci_dir
    if [ -f $ci_dir/1.ci.run.sh ];then
        ./ci_dir/1.ci.run.sh
    fi
done

echo "============================================================================"
